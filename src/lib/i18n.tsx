type Ctx = {
  lang: UiLang;
  setLang: (l: UiLang) => void;
  t: (key: string) => string;
  dir: "ltr" | "rtl";
};
const LangContext = createContext<Ctx | null>(null);

const KEY = "arena.lang";

export function LanguageProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<UiLang>("en");

  useEffect(() => {
    try {
      const stored = localStorage.getItem(KEY) as UiLang | null;
      if (stored && stored in UI_LANGUAGES) setLangState(stored);
    } catch {}
  }, []);

  const setLang = (l: UiLang) => {
    setLangState(l);
    try {
      localStorage.setItem(KEY, l);
    } catch {}
  };

  const t = (key: string) => DICTS[lang]?.[key] ?? key;
  const dir: "ltr" | "rtl" = lang === "ar" ? "rtl" : "ltr";

  return <LangContext.Provider value={{ lang, setLang, t, dir }}>{children}</LangContext.Provider>;
}

export function useT() {
  const ctx = useContext(LangContext);
  if (!ctx)
    return { lang: "en" as UiLang, setLang: () => {}, t: (k: string) => k, dir: "ltr" as const };
  return ctx;
}
